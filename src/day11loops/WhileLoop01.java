package day11loops;

public class WhileLoop01 {

	public static void main(String[] args) {
		/*
		 Print "Hello" 5 times to the console.
		 
		 */
		for(int i=1;i<=5;i++) {
			System.out.println("Hello");
		}
		System.out.println("___________________________________");
		int i=1;
		while(i<=5) {
			System.out.println("Hello");
			i++;
		}

	}

}
